const AES_EXPLICIT_IV = Buffer.from('a1b2c3d4e5f60718293a4b5c6d7e8f90', 'hex');

/**
 * MEDIA-SEQUENCE nonzero so `seq` does NOT coincide with the array index — this lets the unit test
 * distinguish a version using `seg.seq` from one that mistakenly uses the loop index.
 * ⚠️ Don't "tidy it back to 0", and don't assume e2e catches this: MEASURED, e2e does NOT catch it
 * (see "MEASURED LIMIT" above). What catches it is `utils/crypto.test.ts`.
 */
const AES_MEDIA_SEQUENCE = 7;

/** MEDIA-SEQUENCE of the explicit-IV playlist: nonzero so it's distinguishable from an IV derived from seq. */
const AES_IV_MEDIA_SEQUENCE = 3;

/** Segment index at which the second key starts being used (key-rotation case). */
const AES_ROTATE_AT = 5;

/** Default HLS IV: media sequence as 128-bit big-endian (12 zero bytes + uint32). */
function seqIv(seq) {
  const iv = Buffer.alloc(16);
  iv.writeUInt32BE(seq >>> 0, 12);
  return iv;
}

/** AES-128-CBC + PKCS7 — per the HLS spec (RFC 8216 §5.2). */
function aesEncrypt(plain, key, iv) {
  const c = createCipheriv('aes-128-cbc', key, iv);
  c.setAutoPadding(true);
  return Buffer.concat([c.update(plain), c.final()]);
}

