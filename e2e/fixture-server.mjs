//
// Encrypted with `node:crypto` (OpenSSL) while the extension decrypts with WebCrypto -> TWO
// INDEPENDENT implementations, so there's no "same mistake on both sides" way for this case to go
// falsely green.
//
// 🔴 MEASURED LIMIT OF THIS CASE SET ITSELF — READ BEFORE TRUSTING IT COVERS MORE THAN IT DOES:
// The download cases here pin the KEY-FETCH path + CBC decryption, but **do NOT pin the IV-derivation
// path**. Measured via mutation on the real build (2026-07-19): swapping `seg.seq` for the array
// index -> case STAYS GREEN; ignoring `#EXT-X-KEY:IV=` -> case STAYS GREEN. Reason: it's a property
// of CBC — the IV only affects the FIRST 16-byte block of each segment. Measured on this very
// fixture: a wrong IV shifts EXACTLY 10 of 143,444 bytes, still 100 frames, IDENTICAL video-stream
// md5, EMPTY ffmpeg stderr, and the resulting .mp4 IDENTICAL BYTE-FOR-BYTE (verified with `cmp`).
// => NO content-based file assertion catches an IV bug with MPEG-TS. The net for IV lives in the
